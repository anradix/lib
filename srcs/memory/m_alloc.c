#include "mylib.h"

char	*m_alloc(size_t x, char c)
{
	char *s;

	if (!(s = (char*)malloc(sizeof(char) * x + 1)))
		return (NULL);
	s[x] = '\0';
	s_set(s, x, c);
	return (s);
}
