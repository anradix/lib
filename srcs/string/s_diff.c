#include "mylib.h"

bool	s_diff(const char *s1, const char *s2)
{
	if (!s1 || !s2 || *s1 != *s2)
		return (1);
	return (!*s1) ? 0 : s_diff(s1 + 1, s2 + 1) ;
}
