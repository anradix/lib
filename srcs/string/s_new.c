#include "lib.h"

char	*s_new(size_t size)
{
	char	*s;

	if (!(s = (char*)malloc(sizeof(char) * ((size + 1)))))
		return (NULL);
	size++;
	while (size--)
		s[size] = '\0';
	return (s);
}
